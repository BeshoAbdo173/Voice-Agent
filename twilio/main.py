@app.post("/make-call")
async def make_call(request: Request):
    """Trigger an outbound call via Twilio REST API.

    Expected JSON body: {"to": "+1555..."}
    """
    try:
        body = await request.json()
        to = body.get("to")
        if not to:
            raise ValueError("Missing 'to' in request body")
        if not twilio_number:
            return JSONResponse({"error": "TWILIO_NUMBER not configured"}, status_code=500)
        call = client.calls.create(
            to=to,
            from_=twilio_number,
            url="https://zella-somnolent-danuta.ngrok-free.dev/incoming-call"
        )
        return JSONResponse({"sid": call.sid, "status": "queued"})
    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)


@app.post("/send-sms")
async def send_sms(request: Request):
    """Send an SMS via Twilio REST API.

    Expected JSON body: {"to":"+1555...","body":"message text"}
    """
    try:
        body = await request.json()
        to = body.get("to")
        text = body.get("body") or body.get("message")
        if not to or not text:
            raise ValueError("Missing 'to' or 'body' in request body")
        if not twilio_number:
            return JSONResponse({"error": "TWILIO_NUMBER not configured"}, status_code=500)
        msg = client.messages.create(to=to, from_=twilio_number, body=text)
        return JSONResponse({"sid": msg.sid, "status": "sent"})
    except Exception as e:
        return JSONResponse({"error": str(e)}, status_code=500)


